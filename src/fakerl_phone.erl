-export([number/0]).

-define(FORMATS, {"###-###-####", "(###)###-####", "1-###-###-####", "###.###.####", "###-###-####", "(###)###-####", "1-###-###-####", "###.###.####", "###-###-#### x###", "(###)###-#### x###", "1-###-###-#### x###", "###.###.#### x###", "###-###-#### x####", "(###)###-#### x####", "1-###-###-#### x####", "###.###.#### x####", "###-###-#### x#####", "(###)###-#### x#####", "1-###-###-#### x#####", "###.###.#### x#####"}).

number() ->
    Format = element(random:uniform(tuple_size(?FORMATS)), ?FORMATS),
    lists:map(fun($#) ->
                      integer_to_list(random:uniform(9));
                 (X) ->
                      X
              end, Format).

